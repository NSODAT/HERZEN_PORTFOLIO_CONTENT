#Создание классов:
#Класс в ООП - это шаблон или чертёж, который определяет структуру и поведение объектов. 
#Классы используются для создания экземпляров, называемых объектами, которые имеют атрибуты (данные) и методы (функции).
class Book:
    def __init__(self, title, author):
        self.title = title
        self.author = author

#В примере выше, Book - это класс с конструктором __init__, который инициализирует новый объект с заголовком и автором.

#Создание объектов:
#Объект - это экземпляр класса. Когда класс определён, вы можете создавать объекты этого класса, которые называются экземплярами.
my_book = Book("Война и мир", "Лев Толстой")

#Здесь my_book - это объект класса Book, созданный с использованием конструктора класса.

#Статический метод:
#Статические методы - это методы, которые принадлежат классу, а не объекту класса. 
#Они не требуют создания экземпляра класса для их вызова.
class MathOperations:
    @staticmethod
    def add(x, y):
        return x + y

# Вызов статического метода без создания объекта
result = MathOperations.add(5, 7)
#Аксессоры (getter, setter, deliter):
#Аксессоры - это методы, которые управляют доступом к атрибутам объекта. 
#Getter возвращает значение, setter устанавливает его, а deleter удаляет атрибут.
class Person:
    def __init__(self, name):
        self._name = name

    @property
    def name(self):
        return self._name

    @name.setter
    def name(self, value):
        self._name = value

    @name.deleter
    def name(self):
        del self._name
