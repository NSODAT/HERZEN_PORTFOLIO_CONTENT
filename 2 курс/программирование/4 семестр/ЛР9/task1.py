#Наследование:
#Наследование позволяет новому классу принимать атрибуты и методы существующего класса. 
#Это способствует повторному использованию кода.
class Vehicle:
    def move(self):
        print("Двигается")

class Car(Vehicle):
    def move(self):
        print("Едет по дороге")
#Полиморфизм:
#Полиморфизм - это способность использовать общий интерфейс для различных базовых форм (типов данных). 
#В Python это достигается с помощью методов, которые могут быть переопределены в подклассах.
class Animal:
    def speak(self):
        pass

class Dog(Animal):
    def speak(self):
        return "Гав"

class Cat(Animal):
    def speak(self):
        return "Мяу"
#Интерфейс:
#Интерфейс определяет набор методов, которые класс должен реализовать, 
#не предоставляя их реализацию. В Python это достигается с помощью абстрактных классов и абстрактных методов.
from abc import ABC, abstractmethod

class Shape(ABC):
    @abstractmethod
    def area(self):
        pass
#Множественное наследование:
#Множественное наследование позволяет классу наследовать атрибуты и методы более чем одного класса.
class A:
    pass

class B:
    pass

class C(A, B):
    pass
#Примесь (mixin):
#Примесь - это класс, который предоставляет определённые функциональности для повторного использования в других классах, 
#но не предназначен для самостоятельного использования.
class JsonMixin:
    def to_json(self):
        import json
        return json.dumps(self.__dict__)

class Person(JsonMixin):
    def __init__(self, name):
        self.name = name
#Перегрузка:
#Перегрузка позволяет определить методы с одинаковым именем, но разными параметрами. 
#В Python это можно имитировать, используя аргументы по умолчанию или переменное количество аргументов.
